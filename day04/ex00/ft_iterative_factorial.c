int		ft_iterative_factorial(int nb)
{
	int i;
	int k;

	i = 1;
	k = nb;
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	while (nb > i)
	{
		k *= (nb - i);
		i++;
	}
	return (k);
}
